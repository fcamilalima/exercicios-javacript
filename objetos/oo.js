// fora do fluxo.
// Programação Procedural -> baseada na procedure, nos 
// procedimentos, nas funções.
processamento(valor1, valor2, valor3)

// Orientação a Objetos
// Programação Procedural -> Funções manipulando dados.
// OO
// Dados e dentro desses dados, temos funções. 
// O objeto funciona como uma cápsula que agrupa
// atributos e comportamentos.
// Interpretar o mundo Orientado a Objetos. O mundo é 
// formado com objetos que incorporam atributos e 
// comportamentos, interagem com outros objetos, 
// experienciam a herança de comportamentos.


objeto = {
    valor1,
    valor2,
    valor3,
    processamento() {
        // ...
    }
}

objeto.processamento() // Foco passou a ser o objeto

// Princípios importantes:
// 1. Abstração -> Trazer um objeto da vida real para a 
// dinâmica de um software.
// 2. Encapsulamento -> detalhes de implementação escondida 
// e mostra uma interface simples para que possa interagir 
// com aquele objeto. Quando se tem alto nível de 
// acoplamento (dependência entre os objetos do sistema, 
// comunicação entre os objetos em si), mais baixo 
// será o encapsulamento do código.
// 3. Herança (prototype) ->  Baseada numa relação é-um. 
// Receber atributos e comportamentos de uma função-pai. 
// A herança em Javascript trabalha com herança prototype.
// Nem toda relação é-um se constitui em uma herança. 
// Priorize a composição (tem-um) em vez de herança (é-um).
// A herança pode trazer confusões no código. Nem sempre 
// a relação é-um pode ser tratada como herança. Existem 
// padrões de projetos que podem inverter essa situação.
// 4. Polimorfismo -> Múltiplas formas. 
