/* 
 * Operadores de atribuição. 
 * Atribuição com operações aritméticas.
*/

const a = 7
let b = 3

b += a // b = b + a  //Atribuição aditiva (Forma reduzida)
console.log('b = ', b) // Resposta: 10

b -= 4 // b = b - 4 //Atribuição subtrativa (Forma reduzida)
console.log('b = ', b) // Resposta: 6

b *= 2 // b = b * 2 //Atribuição multiplicativa (Forma reduzida)
console.log('b = ', b) // Resposta: 12

b /= 2 // b = b/2 //Atribuição divisiva (Forma reduzida)
console.log('b = ', b) // Resposta: 6

b %= 2 // b = b % 2 //Atribuição modular (Forma reduzida)
console.log('b = ', b) // Resposta: 0


